    @Composable
    fun CheckBoxExample() {
        var accepted by remember { mutableStateOf(false) }

        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(32.dp),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Row(
                verticalAlignment = Alignment.CenterVertically,
                modifier = Modifier.padding(16.dp)
            ) {
                Checkbox(
                    checked = accepted,
                    onCheckedChange = {accepted = it},
                    colors = CheckboxDefaults.colors(
                        checkedColor = MaterialTheme.colorScheme.primary,
                        uncheckedColor = MaterialTheme.colorScheme.onSurfaceVariant,
                        checkmarkColor = MaterialTheme.colorScheme.onPrimary
                    )
                )

                Spacer(modifier = Modifier.width(8.dp))

                Text(text = if (accepted)"Aceptado" else "Aceptar los terminos y condiciones")
            }
        }
    }

    @Composable
    fun PreferencesApp() {
        val options = listOf(
            "Notificaciones por correo",
            "Notificaciones push",
            "Ofertas especiales",
            "Novedades del blog"
        )

        val stateOptions = remember {
            mutableStateMapOf<String, Boolean>().apply {
                options.forEach {
                    put(it, false)
                }
            }
        }
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(32.dp),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Text(
                text = "Seleciona tus preferencias",
                style = MaterialTheme.typography.titleMedium,
                modifier = Modifier.padding(bottom = 16.dp)
            )

            LazyColumn {
                items(options) {option->
                    Row(
                        verticalAlignment = Alignment.CenterVertically,
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(vertical = 8.dp)
                    ) {
                        Checkbox(
                            checked = stateOptions[option] == true,
                            onCheckedChange = {stateOptions[option] = it},
                            colors = CheckboxDefaults.colors(
                                checkedColor = MaterialTheme.colorScheme.primary,
                                checkmarkColor = MaterialTheme.colorScheme.onPrimary
                            )
                        )

                        Spacer(modifier = Modifier.width(8.dp))

                        Text(text = option)
                    }
                }
            }

            Spacer(modifier = Modifier.height(24.dp))

            val selected = stateOptions.filter { it.value }.keys

            Text(
                text = "Seleccinadas: ${if(selected.isEmpty()) "Ninguna" else selected.joinToString ()}",
                style = MaterialTheme.typography.bodyMedium
            )
        }
    }
